/* USER CODE BEGIN 4 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM1) {

    uint32_t duty_in  = (fade_step * pwm_arr) / FADE_STEPS;
    uint32_t duty_out = ((FADE_STEPS - fade_step) * pwm_arr) / FADE_STEPS;

    switch (fade_phase) {
    case 0:
      __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, duty_out);
      __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, duty_in);
      __HAL_TIM_SET_COMPARE(&htim14, TIM_CHANNEL_1, 0);
      break;
    case 1:
      __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, duty_out);
      __HAL_TIM_SET_COMPARE(&htim14, TIM_CHANNEL_1, duty_in);
      __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 0);
      break;
    case 2:
      __HAL_TIM_SET_COMPARE(&htim14, TIM_CHANNEL_1, duty_out);
      __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, duty_in);
      __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, 0);
      break;
    }

    if (++fade_step >= FADE_STEPS) {
      fade_step = 0;
      fade_phase = (fade_phase + 1) % 3;
    }
  }
}
